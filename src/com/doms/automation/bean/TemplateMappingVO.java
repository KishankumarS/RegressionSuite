package com.doms.automation.bean;

public class TemplateMappingVO {
	private String templateName = "";
	private String mappingSheet = "";
	private String DataRules = "";
	
	public String getTemplateName() {
		return templateName;
	}
	public void setTemplateName(String templateName) {
		this.templateName = templateName;
	}
	public String getMappingSheet() {
		return mappingSheet;
	}
	public void setMappingSheet(String mappingSheet) {
		this.mappingSheet = mappingSheet;
	}
	public String getDataRules() {
		return DataRules;
	}
	public void setDataRules(String dataRules) {
		DataRules = dataRules;
	}
	
}
