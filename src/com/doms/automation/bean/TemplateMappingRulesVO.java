package com.doms.automation.bean;

public class TemplateMappingRulesVO {
	String xmlType;
	String xmlName;
	String mappingSheet;
	String rules;
	public String getXmlType() {
		return xmlType;
	}
	public void setXmlType(String xmlType) {
		this.xmlType = xmlType;
	}
	public String getXmlName() {
		return xmlName;
	}
	public void setXmlName(String xmlName) {
		this.xmlName = xmlName;
	}
	public String getMappingSheet() {
		return mappingSheet;
	}
	public void setMappingSheet(String mappingSheet) {
		this.mappingSheet = mappingSheet;
	}
	public String getRules() {
		return rules;
	}
	public void setRules(String rules) {
		this.rules = rules;
	}
	
	
	
}
